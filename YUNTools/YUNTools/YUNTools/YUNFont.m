#import "YUNFont.h"

UIFont *YUNFont(CGFloat size)
{
    return [UIFont systemFontOfSize:size];
}


UIFont *YUNBoldFont(CGFloat size)
{
    return [UIFont boldSystemFontOfSize:size];
}